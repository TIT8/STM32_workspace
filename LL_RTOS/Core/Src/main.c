/**
* @brief Function implementing the led_task thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask02 */
void StartTask02(void *argument)
{
  /* USER CODE BEGIN StartTask02 */
  /*
   	   Without using num, when you press the button the LED toggle.
   	   If you hold the button down, the LED will blink for the time
   	   you continue to press.

   	   With num (uncomment all the section where there is num), when
   	   the button is pressed, the LED blink 11 times and then stop,
   	   indipendently from the status button.
  */
  //uint32_t num = 0;
  osDelay(1);
  /* Infinite loop */
  for(;;)
  {
	/*if(num == 0)*/ osSemaphoreAcquire(go_semaHandle, osWaitForever);
	LL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
	//num++;
	osDelay(1000);
	//if(num > 10) num = 0;
  }
  /* USER CODE END StartTask02 */
}

